public class OperacoesAritmeticas {
    public static void main(String[] args) {
        // Operações aritméticas
        System.out.println("4 + 6 * 2 = " + (4 + 6 * 2));
        System.out.println("39 / 10 = " + (39.0 / 10));
        System.out.println("15 > 13 = " + (15 > 13));
        System.out.println("5 < 8 - 3 = " + (5 < 8 - 3));
        System.out.println("10 / 5 + 8 = " + (10.0 / 5 + 8));
        System.out.println("19 % (2 + 3) = " + (19 % (2 + 3)));
        System.out.println("8 <= (2 + 6) = " + (8 <= (2 + 6)));
        System.out.println("7 != 7 = " + (7 != 7));
        System.out.println("17 / 2 = " + (17.0 / 2));
        System.out.println("36 % (6 + 2) = " + (36 % (6 + 2)));
        System.out.println("3 >= 3 = " + (3 >= 3));
        System.out.println("10 - 20 == -10 = " + (10 - 20 == -10));
        System.out.println("22 / 5 = " + (22.0 / 5));
        System.out.println("8 % 2 * 0 = " + (8 % 2 * 0));
        System.out.println("3 * 3 == 2 * 4 = " + (3 * 3 == 2 * 4));
        System.out.println("3 + 2 * 6 == 15 = " + (3 + 2 * 6 == 15));
    }
}
